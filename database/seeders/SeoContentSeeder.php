<?php

namespace Database\Seeders;

use Illuminate\Database\Seeder;
use App\Models\Content;

/**
 * SeoContentSeeder
 * ————————————————
 * يزرع محتوى SEO (عناوين، أوصاف، FAQ، نص تفصيلي) لجميع صفحات الخدمات.
 * يستخدم firstOrCreate حتى لا يكرر المحتوى عند إعادة التشغيل.
 */
class SeoContentSeeder extends Seeder
{
    public function run(): void
    {
        $siteName = 'شركة مظلات وسواتر التميز';

        // ═══════════════════════════════════════════════════════
        // SEO data per service slug
        // ═══════════════════════════════════════════════════════
        $seoData = $this->getSeoData($siteName);

        foreach ($seoData as $slug => $data) {
            $pageName = $data['page_name'];

            // ——— 1. SEO Title ———
            $this->seedContent($slug, $pageName, 'seo.title', 'SEO', 'text', $data['seo_title']);

            // ——— 2. SEO Description ———
            $this->seedContent($slug, $pageName, 'seo.description', 'SEO', 'text', $data['seo_description']);

            // ——— 3. Hero Title ———
            $this->seedContent($slug, $pageName, 'hero.title', 'الهيدر', 'text', $data['hero_title']);

            // ——— 4. Hero Subtitle ———
            $this->seedContent($slug, $pageName, 'hero.subtitle', 'الهيدر', 'text', $data['hero_subtitle']);

            // ——— 5. Hero Badge ———
            $this->seedContent($slug, $pageName, 'hero.badge', 'الهيدر', 'text', $data['hero_badge']);

            // ——— 6. CTA Title ———
            $this->seedContent($slug, $pageName, 'cta.title', 'CTA', 'text', $data['cta_title']);

            // ——— 7. CTA Subtitle ———
            $this->seedContent($slug, $pageName, 'cta.subtitle', 'CTA', 'text', $data['cta_subtitle']);

            // ——— 8. Overview text ———
            $this->seedContent($slug, $pageName, 'overview.text', 'نبذة', 'text', $data['overview_text']);

            // ——— 9. Rich Content Body (HTML) ———
            if (!empty($data['content_body'])) {
                $this->seedContent($slug, $pageName, 'content.body', 'المحتوى التفصيلي', 'text', $data['content_body']);
            }

            // ——— 10. FAQ items ———
            if (!empty($data['faqs'])) {
                foreach ($data['faqs'] as $i => $faq) {
                    $num = $i + 1;
                    $this->seedContent($slug, $pageName, "faq.{$num}.question", 'الأسئلة الشائعة', 'text', $faq['q']);
                    $this->seedContent($slug, $pageName, "faq.{$num}.answer", 'الأسئلة الشائعة', 'text', $faq['a']);
                }
            }
        }

        $this->command->info('✅ SEO content seeded successfully for all services.');
    }

    /**
     * Helper: seed a single content row
     */
    private function seedContent(string $slug, string $pageName, string $keySuffix, string $section, string $type, string $value): void
    {
        Content::updateOrCreate(
            ['key' => "services.{$slug}.{$keySuffix}"],
            [
                'page'       => $pageName,
                'section'    => $section,
                'type'       => $type,
                'value'      => $value,
                'status'     => 'published',
                'created_by' => 1,
            ]
        );
    }

